package com.mslji.mybluetooth.Model;

public class GetHistoryData {
    public String onetv;
    public String twotv;
    public String threetv;
    public String newtime;
    public String id;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getOnetv() {
        return onetv;
    }

    public void setOnetv(String onetv) {
        this.onetv = onetv;
    }

    public String getTwotv() {
        return twotv;
    }

    public void setTwotv(String twotv) {
        this.twotv = twotv;
    }

    public String getThreetv() {
        return threetv;
    }

    public void setThreetv(String threetv) {
        this.threetv = threetv;
    }

    public String getNewtime() {
        return newtime;
    }

    public void setNewtime(String newtime) {
        this.newtime = newtime;
    }
}
