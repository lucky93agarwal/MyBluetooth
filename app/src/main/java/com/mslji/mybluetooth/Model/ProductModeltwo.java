package com.mslji.mybluetooth.Model;

public class ProductModeltwo {
    public String id;
    public String date;
    public String numbers;
    public String DataTaken;


    public String getDataTaken() {
        return DataTaken;
    }

    public void setDataTaken(String dataTaken) {
        DataTaken = dataTaken;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getNumbers() {
        return numbers;
    }

    public void setNumbers(String numbers) {
        this.numbers = numbers;
    }
}
